""""
A calculator module
"""


def add(x, y):
    return x + y


def subtract(x, y):
    return x - y


def multiply(x, y):
    return x * y


def divide(x, y):
    return x / y


# build a function to calculate the power of a number
def power(x, y):
    return x**y


# build a function to calculate the square root of a number
def sqrt(x):
    return x**0.5
